// 构建图片名称 -> 完整文件名的映射（文件名格式: {编号}_{图片名}.png）
const imageFileMap: Record<string, string> = {};
const imageModules = import.meta.glob('/public/images/enemies/*.png', { eager: true, query: '?url', import: 'default' });
for (const [path, url] of Object.entries(imageModules)) {
  const fileName = path.split('/').pop()!;
  const nameWithoutExt = fileName.replace('.png', '');
  const parts = nameWithoutExt.split('_');
  if (parts.length >= 2) {
    let imageName: string;
    if (/^\d+$/.test(parts[0])) {
      imageName = parts.slice(1).join('_');
    } else {
      imageName = nameWithoutExt;
    }
    if (!imageFileMap[imageName]) {
      imageFileMap[imageName] = url as string;
    }
  }
}

export const getEnemyImageUrl = (enemyId: string): string | undefined => {
  const imageName = ENEMY_IMAGE_MAP[enemyId];
  if (!imageName) return undefined;
  return imageFileMap[imageName];
};

export const getBossImageUrl = (bossId: number, difficulty: number = 0): string | undefined => {
  const difficultyMap = BOSS_IMAGE_MAP[bossId];
  if (!difficultyMap) return undefined;
  const imageName = difficultyMap[difficulty] || difficultyMap[0];
  if (!imageName) return undefined;
  return imageFileMap[imageName];
};
