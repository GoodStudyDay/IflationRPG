def update_language_data():
    input_file = '/Users/chencheng/Documents/trae_projects/IflationRPG/src/data/languageData.ts'
    
    with open(input_file, 'r', encoding='utf-8') as f:
        lines = f.readlines()
    
    new_lines = []
    for line in lines:
        for old_key, new_key in KEY_MAPPING.items():
            if f"  {old_key}:" in line and "translations" in ''.join(new_lines):
                line = line.replace(f"  {old_key}:", f"  '{new_key}':")
        new_lines.append(line)
    
    with open(input_file, 'w', encoding='utf-8') as f:
        f.writelines(new_lines)

def update_component_files():
    src_dir = '/Users/chencheng/Documents/trae_projects/IflationRPG/src'
    
    files_to_update = [
        'components/TitleScreen.tsx',
        'components/SettingsModal.tsx',
        'components/MenuOverlay.tsx',
        'components/MainScreen.tsx',
        'stores/gameStore.ts'
    ]
    
    for rel_path in files_to_update:
        full_path = os.path.join(src_dir, rel_path)
        if not os.path.exists(full_path):
            continue
        
        with open(full_path, 'r', encoding='utf-8') as f:
            content = f.read()
        
        for old_key, new_key in KEY_MAPPING.items():
            content = content.replace(f"t('{old_key}')", f"t('{new_key}')")
            content = content.replace(f't("{old_key}")', f't("{new_key}")')
        
        with open(full_path, 'w', encoding='utf-8') as f:
            f.write(content)

if __name__ == '__main__':
    print("Updating languageData.ts...")
    update_language_data()
    print(f"Updated {len(KEY_MAPPING)} keys")
    
    print("Updating component files...")
    update_component_files()
    print("Done!")
